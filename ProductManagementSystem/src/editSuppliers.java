    private void suppliersDeleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suppliersDeleteBTNActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(dburl, dbusername, dbpassword);
            Statement statement = con.createStatement();
            
            
      
            String query = "DELETE FROM Suppliers WHERE supplier_name = ? AND contact_info = ?";
           
            PreparedStatement preparedStatement = con.prepareStatement(query);
            preparedStatement.setString(1, this.rowName);
            preparedStatement.setString(2, this.rowContact);

            preparedStatement.executeUpdate();
            
            suppliersNameTF.setText("");
            suppliersContactTF.setText("");
            
            parentPage.loadTable();
            loadCB();
            parentPage.loadSuppliersCB(parentPage.getPOrdersSuppliersCB());
            this.setVisible(false);
            
            statement.close();
            con.close();
            
        } catch(Exception e) {
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_suppliersDeleteBTNActionPerformed

    private void suppliersUpdateBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suppliersUpdateBTNActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(dburl, dbusername, dbpassword);
            Statement statement = con.createStatement();
            String name = suppliersNameTF.getText();
            String contact = suppliersContactTF.getText();
            
      
            String query = "UPDATE Suppliers SET supplier_name = ?, contact_info = ? WHERE supplier_name = ? AND contact_info = ?";
           
            PreparedStatement preparedStatement = con.prepareStatement(query);
            preparedStatement.setString(1, name);
            preparedStatement.setString(2, contact);
            preparedStatement.setString(3, this.rowName);
            preparedStatement.setString(4, this.rowContact);
            preparedStatement.executeUpdate();
           
            
            parentPage.loadTable();
            parentPage.loadSuppliersCB(parentPage.getPOrdersSuppliersCB());
            this.setVisible(false);
            
            statement.close();
            con.close();
            
        } catch(Exception e) {
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_suppliersUpdateBTNActionPerformed

    private void suppliersCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suppliersCBActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(dburl, dbusername, dbpassword);

            //Statement statement = con.createStatement();

            String selected = suppliersCB.getSelectedItem().toString();

            String[] id = selected.split(" - ");

            String query = "SELECT * FROM Suppliers WHERE supplier_id = ?";

            PreparedStatement preparedStatement = con.prepareStatement(query);
            preparedStatement.setString(1, id[0]);

            ResultSet sqlresult = preparedStatement.executeQuery();

            while(sqlresult.next()) {
                suppliersNameTF.setText(sqlresult.getString("supplier_name"));
                suppliersContactTF.setText(sqlresult.getString("contact_info"));
            }

        } catch (Exception e) {
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_suppliersCBActionPerformed

