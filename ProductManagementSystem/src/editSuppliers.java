    private static final String dburl = "jdbc:mysql://localhost:3306/finals";
    private static final String dbusername = "root";
    private static final String dbpassword = "rootroot";
    private String rowName = "";
    private String rowContact = "";

    public final void loadCB() {

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(dburl, dbusername, dbpassword);

            Statement statement;
            statement = con.createStatement();
            ResultSet sqlresult;
            sqlresult = statement.executeQuery("select * from suppliers");
            
            suppliersCB.removeAllItems();

            while(sqlresult.next()) {
                suppliersCB.addItem(sqlresult.getString("supplier_id") + " - " + sqlresult.getString("supplier_name"));
            }
            
        }    
            catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    
    }
    
